def print_materialise_summary(report: MaterialiseReport) -> None:
    """Print a concise summary table to stdout."""
    print("\n" + "=" * 60)
    print("  E1 MATERIALISE INPUTS - SUMMARY")
    print("=" * 60)
    print(f"  {'Task':<6} {'Split':<6} {'BM rows':>8} {'Joined':>8} {'Missing':>8}  {'File'}")
    print("  " + "-" * 56)
    for r in report.split_results:
        fname = r.output_path.name if r.output_path else "(skipped)"
        print(
            f"  {r.task.upper():<6} {r.split:<6} "
            f"{r.n_benchmark:>8,} {r.n_joined:>8,} {r.n_missing:>8,}  {fname}"
        )

    if report.aux_and_result and not report.aux_and_result.get("skipped"):
        print()
        print(f"  Auxiliary Scopus-ID comparator (AND):")
        print(f"    n_benchmark : {report.aux_and_result.get('n_benchmark', '?')}")
        print(f"    n_unmatched : {report.aux_and_result.get('n_unmatched', '?')}")
        print(f"    pred dist   : {report.aux_and_result.get('pred_distribution', '?')}")

    print()
    if report.all_warnings:
        print(f"  WARNINGS ({len(report.all_warnings)}):")
        for w in report.all_warnings:
            print(f"    - {w}")
    else:
        print("  No warnings.")

    print()
    if report.manifest_path:
        print(f"  Manifest: {report.manifest_path}")
    print("=" * 60 + "\n")
