"""e1_materialise_inputs.py — Stage E1: materialise split-specific evaluation inputs.

For each task (AND, AIN) this stage produces ready-to-use, stand-alone parquet
files — one per task × split — that downstream metric scripts can consume
directly without re-joining on every run.

Outputs written to ``outputs/eval_inputs/``
-------------------------------------------
predictions_and_dev.parquet          AND dev predictions + gold labels
predictions_and_test.parquet         AND test predictions  (skipped if no test rows)
predictions_ain_dev.parquet          AIN dev predictions + gold labels
predictions_ain_test.parquet         AIN test predictions  (skipped if no test rows)
aux_scopus_id_comparator_and.parquet auxiliary AND Scopus-ID pass comparator
eval_inputs_manifest.json            full provenance record

Schema of predictions_<task>_<split>.parquet
---------------------------------------------
Columns from benchmark (authoritative):
  anchor_id, candidate_id, task, gold_label, split

Columns from routing log (BEM decisions):
  label, confidence, evidence_used, reason_code, abstention_reason,
  backend, retry_count, label_final, override_reason, signals_count,
  fired_categories, routed_to_human

Schema of aux_scopus_id_comparator_and.parquet
----------------------------------------------
  anchor_id, candidate_id, gold_label, split,
  scopus_id_comparator_pred,   # bool: True if pair was generated by pass1_author_id
  best_pass_id,                # raw pass label from candidates_and
  best_block_key,              # e.g. "author_id:57221443727"
  similarity_score

Notes
-----
- ``gold_label`` in routing logs is verified to match the benchmark and then
  dropped before merge; only the benchmark's ``gold_label`` is kept.
- If a split value has zero rows (e.g. 'test' does not yet exist), no output
  file is written and a WARNING is emitted.
- The auxiliary Scopus-ID comparator is labelled AUXILIARY and uses a separate
  output file; it must not be confused with BEM predictions.
"""

from __future__ import annotations

import hashlib
import json
import platform
import subprocess
import sys
from dataclasses import dataclass, field
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

import pandas as pd

from bem.eval.config import EvalConfig

# Routing log columns to carry forward (excludes gold_label which comes from bm)
_ROUTING_KEEP = [
    "anchor_id", "candidate_id",
    "label", "confidence", "evidence_used", "reason_code",
    "abstention_reason", "backend", "retry_count",
    "label_final", "override_reason", "signals_count",
    "fired_categories", "routed_to_human",
]

# All expected splits; file is silently skipped if a split has no rows
_ALL_SPLITS = ("dev", "test")


# ---------------------------------------------------------------------------
# Data classes for structured reporting
# ---------------------------------------------------------------------------

@dataclass
class SplitResult:
    task: str
    split: str
    n_benchmark: int
    n_joined: int
    n_missing: int
    output_path: Path | None   # None if skipped (zero rows)
    warnings: list[str] = field(default_factory=list)


@dataclass
class MaterialiseReport:
    split_results: list[SplitResult] = field(default_factory=list)
    aux_and_result: dict[str, Any] = field(default_factory=dict)
    manifest_path: Path | None = None
    all_warnings: list[str] = field(default_factory=list)


# ---------------------------------------------------------------------------
# Public entry point
# ---------------------------------------------------------------------------

def run_materialise_inputs(cfg: EvalConfig) -> MaterialiseReport:
    """Execute Stage E1: materialise split-specific evaluation inputs.

    Args:
        cfg: Validated :class:`~bem.eval.config.EvalConfig` instance.

    Returns:
        :class:`MaterialiseReport` describing what was written and any warnings.

    Raises:
        FileNotFoundError: If any required input file is missing.
        ValueError: If the benchmark is missing the ``split`` column or the
                    routing-log gold_label verification fails.
    """
    out_dir = cfg.eval_inputs_output_dir
    out_dir.mkdir(parents=True, exist_ok=True)

    report = MaterialiseReport()

    task_paths = {
        "and": (cfg.benchmark_and, cfg.routing_and_bm),
        "ain": (cfg.benchmark_ain, cfg.routing_ain_bm),
    }

    for task in cfg.tasks:
        bm_path, rt_path = task_paths[task]
        results = _materialise_task(
            task=task,
            bm_path=bm_path,
            rt_path=rt_path,
            out_dir=out_dir,
            force=cfg.force,
        )
        report.split_results.extend(results)
        for r in results:
            report.all_warnings.extend(r.warnings)

    # Auxiliary Scopus-ID comparator (AND only)
    if "and" in cfg.tasks and cfg.produce_scopus_id_comparator:
        aux_result = _materialise_scopus_id_comparator(
            bm_path=cfg.benchmark_and,
            candidates_and_path=cfg.candidates_and,
            out_dir=out_dir,
            force=cfg.force,
        )
        report.aux_and_result = aux_result
        report.all_warnings.extend(aux_result.get("warnings", []))

    # Write manifest
    report.manifest_path = _write_manifest(cfg, out_dir, report)
    return report


# ---------------------------------------------------------------------------
# Per-task materialisation
# ---------------------------------------------------------------------------

def _materialise_task(
    task: str,
    bm_path: Path,
    rt_path: Path,
    out_dir: Path,
    force: bool,
) -> list[SplitResult]:
    """Load, validate, join, and split a single task's benchmark + routing log."""

    # --- Load benchmark ---
    _require_file(bm_path, f"{task.upper()} benchmark")
    bm = pd.read_parquet(bm_path)
    bm = _normalise_str_cols(bm, ["anchor_id", "candidate_id"])

    # --- Validate split column ---
    if "split" not in bm.columns:
        raise ValueError(
            f"[E1] {task.upper()} benchmark is missing the 'split' column. "
            f"Path: {bm_path}"
        )

    split_dist = bm["split"].value_counts().to_dict()
    print(f"[E1] {task.upper()} benchmark split distribution: {split_dist}")

    # --- Load routing log ---
    _require_file(rt_path, f"{task.upper()} benchmark-filtered routing log")
    rt = pd.read_parquet(rt_path)
    rt = _normalise_str_cols(rt, ["anchor_id", "candidate_id"])

    # --- Verify gold_label consistency ---
    _verify_gold_labels(bm, rt, task)

    # --- Drop routing's gold_label (benchmark is authoritative) ---
    rt_slim = rt[[c for c in _ROUTING_KEEP if c in rt.columns]].copy()

    # --- Inner join ---
    joined = bm.merge(rt_slim, on=["anchor_id", "candidate_id"], how="inner")

    n_bm_total = len(bm)
    n_joined_total = len(joined)
    n_missing_total = n_bm_total - n_joined_total
    if n_missing_total > 0:
        pct = 100 * n_missing_total / n_bm_total
        msg = (
            f"[E1] WARNING: {task.upper()} join coverage {n_joined_total}/{n_bm_total} "
            f"({100 - pct:.1f}%). {n_missing_total} benchmark pairs have no routing "
            "decision — they will be absent from all metrics."
        )
        print(msg)

    # --- SAFETY: assert dev/test are disjoint on (anchor_id, candidate_id) ---
    dev_joined  = joined[joined["split"] == "dev"]
    test_joined = joined[joined["split"] == "test"]
    if len(dev_joined) > 0 and len(test_joined) > 0:
        dev_pairs  = set(zip(dev_joined["anchor_id"],  dev_joined["candidate_id"]))
        test_pairs = set(zip(test_joined["anchor_id"], test_joined["candidate_id"]))
        overlap = dev_pairs & test_pairs
        assert len(overlap) == 0, (
            f"[E1] SPLIT INTEGRITY FAILURE ({task.upper()}): "
            f"{len(overlap)} (anchor_id, candidate_id) pairs appear in BOTH dev and test splits. "
            "This indicates a benchmark construction error. "
            "Re-create the benchmark parquet so that each pair appears in exactly one split."
        )
        print(f"[E1] {task.upper()} split integrity check: PASS (dev/test disjoint)")

    results: list[SplitResult] = []

    for split in _ALL_SPLITS:
        split_df = joined[joined["split"] == split].copy()
        n_bm_split = int((bm["split"] == split).sum())
        n_joined_split = len(split_df)
        n_missing_split = n_bm_split - n_joined_split
        warns: list[str] = []

        if n_bm_split == 0:
            msg = (
                f"[E1] WARNING: {task.upper()} split='{split}' has 0 benchmark rows. "
                "Output file will NOT be created. "
                "(This is expected if the test split has not been annotated yet.)"
            )
            print(msg)
            warns.append(msg)
            results.append(SplitResult(
                task=task, split=split,
                n_benchmark=0, n_joined=0, n_missing=0,
                output_path=None, warnings=warns,
            ))
            continue

        out_path = out_dir / f"predictions_{task}_{split}.parquet"

        if out_path.exists() and not force:
            print(
                f"[E1] {task.upper()} split='{split}' output already exists — skipping "
                f"(--force to overwrite): {out_path.name}"
            )
            results.append(SplitResult(
                task=task, split=split,
                n_benchmark=n_bm_split, n_joined=n_joined_split,
                n_missing=n_missing_split,
                output_path=out_path, warnings=warns,
            ))
            continue

        if n_missing_split > 0:
            pct = 100 * n_missing_split / n_bm_split
            msg = (
                f"[E1] WARNING: {task.upper()} split='{split}': "
                f"{n_missing_split}/{n_bm_split} benchmark pairs ({pct:.1f}%) "
                "have no routing decision."
            )
            print(msg)
            warns.append(msg)

        split_df.reset_index(drop=True).to_parquet(out_path, index=False)
        print(
            f"[E1] Written: {out_path.name}  "
            f"({n_joined_split:,} rows, gold dist: "
            f"{split_df['gold_label'].value_counts().to_dict()})"
        )

        # --- Smoke checks ---
        _smoke_check_predictions(split_df, task, split, out_path)

        results.append(SplitResult(
            task=task, split=split,
            n_benchmark=n_bm_split, n_joined=n_joined_split,
            n_missing=n_missing_split,
            output_path=out_path, warnings=warns,
        ))

    return results


# ---------------------------------------------------------------------------
# Auxiliary Scopus-ID comparator
# ---------------------------------------------------------------------------

def _materialise_scopus_id_comparator(
    bm_path: Path,
    candidates_and_path: Path,
    out_dir: Path,
    force: bool,
) -> dict[str, Any]:
    """Derive the auxiliary AND Scopus-ID comparator from candidates_and.

    The comparator predicts 'match' when a pair was generated by
    pass1_author_id (shared Scopus Author ID) and 'non-match' otherwise.
    This is a deterministic rule embedded in the C4 blocking stage; it is
    NOT a standalone classifier.

    This output is labelled AUXILIARY and must not be conflated with BEM
    predictions.

    Args:
        bm_path:             Path to dev2 AND benchmark parquet.
        candidates_and_path: Path to data/interim/candidates_and.parquet.
        out_dir:             Directory to write the auxiliary output file.
        force:               Overwrite if output exists.

    Returns:
        Dict with counts, output path, warnings, and SHA-256 of the output.
    """
    out_path = out_dir / "aux_scopus_id_comparator_and.parquet"
    result: dict[str, Any] = {
        "output_path": str(out_path),
        "warnings": [],
    }

    if out_path.exists() and not force:
        print(
            f"[E1-AUX] Scopus-ID comparator already exists — skipping "
            f"(--force to overwrite): {out_path.name}"
        )
        result["skipped"] = True
        return result

    _require_file(candidates_and_path, "candidates_and")
    bm = pd.read_parquet(bm_path)
    bm = _normalise_str_cols(bm, ["anchor_id", "candidate_id"])

    cand_cols = ["anchor_id", "candidate_id", "best_pass_id", "best_block_key", "similarity_score"]
    cand = pd.read_parquet(candidates_and_path, columns=cand_cols)
    cand = _normalise_str_cols(cand, ["anchor_id", "candidate_id"])

    merged = bm[["anchor_id", "candidate_id", "gold_label", "split"]].merge(
        cand, on=["anchor_id", "candidate_id"], how="left"
    )

    n_bm = len(bm)
    n_unmatched = merged["best_pass_id"].isna().sum()
    if n_unmatched > 0:
        msg = (
            f"[E1-AUX] WARNING: {n_unmatched}/{n_bm} benchmark AND pairs "
            "not found in candidates_and — comparator prediction will be NaN."
        )
        print(msg)
        result["warnings"].append(msg)

    # Comparator prediction: True = match (pair was found by author_id equality)
    merged["scopus_id_comparator_pred"] = (
        merged["best_pass_id"] == "pass1_author_id"
    )

    # Canonical column order
    out_cols = [
        "anchor_id", "candidate_id", "gold_label", "split",
        "scopus_id_comparator_pred", "best_pass_id", "best_block_key",
        "similarity_score",
    ]
    out_cols = [c for c in out_cols if c in merged.columns]
    merged[out_cols].reset_index(drop=True).to_parquet(out_path, index=False)

    pred_dist = merged["scopus_id_comparator_pred"].value_counts().to_dict()
    gold_by_pred = (
        merged.groupby("scopus_id_comparator_pred")["gold_label"]
        .value_counts()
        .to_dict()
    )
    print(
        f"[E1-AUX] Scopus-ID comparator written: {out_path.name}  "
        f"({len(merged):,} rows)  pred dist: {pred_dist}"
    )
    print(f"[E1-AUX] Gold label by comparator prediction: {gold_by_pred}")

    result.update({
        "n_benchmark": n_bm,
        "n_unmatched": int(n_unmatched),
        "pred_distribution": {str(k): int(v) for k, v in pred_dist.items()},
        "sha256": _sha256(out_path),
    })
    return result


# ---------------------------------------------------------------------------
# Smoke checks
# ---------------------------------------------------------------------------

def _smoke_check_predictions(df: pd.DataFrame, task: str, split: str, path: Path) -> None:
    """Run lightweight invariant checks on a materialised predictions file."""
    tag = f"[smoke {task.upper()} {split}]"
    errors: list[str] = []

    required_cols = {"anchor_id", "candidate_id", "gold_label", "split", "label_final"}
    missing_cols = required_cols - set(df.columns)
    if missing_cols:
        errors.append(f"{tag} Missing columns: {sorted(missing_cols)}")

    if "gold_label" in df.columns:
        bad_gold = df["gold_label"].isna().sum()
        if bad_gold > 0:
            errors.append(f"{tag} {bad_gold} rows with null gold_label")
        valid_gold = {"match", "non-match", "uncertain"}
        bad_vals = set(df["gold_label"].dropna().unique()) - valid_gold
        if bad_vals:
            errors.append(f"{tag} Unexpected gold_label values: {bad_vals}")

    if "label_final" in df.columns:
        valid_final = {"match", "non-match", "uncertain"}
        bad_final = set(df["label_final"].dropna().unique()) - valid_final
        if bad_final:
            errors.append(f"{tag} Unexpected label_final values: {bad_final}")

    if "confidence" in df.columns:
        out_of_range = ((df["confidence"] < 0) | (df["confidence"] > 1)).sum()
        if out_of_range > 0:
            errors.append(f"{tag} {out_of_range} rows with confidence outside [0, 1]")

    # Duplicate pairs
    n_dupes = df.duplicated(subset=["anchor_id", "candidate_id"]).sum()
    if n_dupes > 0:
        errors.append(f"{tag} {n_dupes} duplicate (anchor_id, candidate_id) pairs")

    # Round-trip check
    rt = pd.read_parquet(path)
    if len(rt) != len(df):
        errors.append(
            f"{tag} Round-trip row count mismatch: wrote {len(df)}, read back {len(rt)}"
        )

    if errors:
        for e in errors:
            print(f"[E1] SMOKE CHECK FAILED: {e}")
        raise AssertionError(
            f"Smoke checks failed for {task.upper()} split={split}: "
            + "; ".join(errors)
        )
    print(f"[E1] Smoke checks passed: {tag}")


# ---------------------------------------------------------------------------
# Manifest
# ---------------------------------------------------------------------------

def _write_manifest(
    cfg: EvalConfig,
    out_dir: Path,
    report: MaterialiseReport,
) -> Path:
    """Write eval_inputs_manifest.json to *out_dir*."""

    split_records = []
    for r in report.split_results:
        split_records.append({
            "task": r.task,
            "split": r.split,
            "n_benchmark": r.n_benchmark,
            "n_joined": r.n_joined,
            "n_missing": r.n_missing,
            "missing_rate": (
                round(r.n_missing / r.n_benchmark, 4)
                if r.n_benchmark > 0 else None
            ),
            "output_path": str(r.output_path) if r.output_path else None,
            "sha256": _sha256(r.output_path) if r.output_path and r.output_path.exists() else None,
            "warnings": r.warnings,
        })

    manifest: dict[str, Any] = {
        "stage": "E1_materialise_inputs",
        "status": "completed",
        "timestamp_iso": datetime.now(tz=timezone.utc).isoformat(),
        "git_commit": _git_commit(),
        "environment": {"python": sys.version, "platform": platform.platform()},
        "inputs": {
            "benchmark_and": {
                "path": str(cfg.benchmark_and),
                "sha256": _sha256(cfg.benchmark_and),
            },
            "benchmark_ain": {
                "path": str(cfg.benchmark_ain),
                "sha256": _sha256(cfg.benchmark_ain),
            },
            "routing_and_bm": {
                "path": str(cfg.routing_and_bm),
                "sha256": _sha256(cfg.routing_and_bm),
            },
            "routing_ain_bm": {
                "path": str(cfg.routing_ain_bm),
                "sha256": _sha256(cfg.routing_ain_bm),
            },
            "candidates_and": {
                "path": str(cfg.candidates_and),
                "sha256": _sha256(cfg.candidates_and) if cfg.produce_scopus_id_comparator else "skipped",
            },
        },
        "join_keys": ["anchor_id", "candidate_id"],
        "join_type": "inner",
        "gold_label_source": "benchmark (authoritative); routing log gold_label verified identical then dropped",
        "split_results": split_records,
        "aux_scopus_id_comparator": report.aux_and_result,
        "all_warnings": report.all_warnings,
        "config_path": str(cfg.config_path),
        "output_dir": str(out_dir),
    }

    out_path = out_dir / "eval_inputs_manifest.json"
    out_path.write_text(json.dumps(manifest, indent=2, default=str), encoding="utf-8")
    print(f"[E1] Manifest written: {out_path}")
    return out_path


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _require_file(path: Path, label: str) -> None:
    if not path.exists():
        raise FileNotFoundError(
            f"[E1] Required file missing ({label}): {path}\n"
            "Check eval_config.yaml paths and re-run."
        )


def _normalise_str_cols(df: pd.DataFrame, cols: list[str]) -> pd.DataFrame:
    """Cast join-key columns to plain Python str to avoid ArrowString/str merge issues."""
    for c in cols:
        if c in df.columns:
            df[c] = df[c].astype(str)
    return df


def _verify_gold_labels(bm: pd.DataFrame, rt: pd.DataFrame, task: str) -> None:
    """Verify that routing log's gold_label matches benchmark's gold_label.

    Raises ValueError if any mismatch is found — this would indicate a
    provenance inconsistency between the benchmark and the routing log.
    """
    if "gold_label" not in rt.columns:
        return  # nothing to verify

    check = bm[["anchor_id", "candidate_id", "gold_label"]].merge(
        rt[["anchor_id", "candidate_id", "gold_label"]],
        on=["anchor_id", "candidate_id"],
        suffixes=("_bm", "_rt"),
        how="inner",
    )
    mismatches = (check["gold_label_bm"] != check["gold_label_rt"]).sum()
    if mismatches > 0:
        raise ValueError(
            f"[E1] {task.upper()} gold_label mismatch between benchmark and "
            f"routing log: {mismatches} rows differ. "
            "The routing log may have been produced from a different benchmark version."
        )


def _sha256(path: Path | None) -> str | None:
    if path is None or not path.exists():
        return None
    return hashlib.sha256(path.read_bytes()).hexdigest()


def _git_commit() -> str | None:
    """Return HEAD commit hash if the working directory is a git repo."""
    try:
        result = subprocess.run(
            ["git", "rev-parse", "--short", "HEAD"],
            capture_output=True, text=True, timeout=5,
        )
        if result.returncode == 0:
            return result.stdout.strip()
    except Exception:
        pass
    return None


